VALID_KINDS = {"official", "slang", "betting"}
NUMBER_MARKS = ["%", "％"]

# 追加語descのみに適用する、煽り・射幸表現の禁止語（見出し語＝キーはチェック対象外。
# 「鉄板」「銀行レース」等は用語そのものとして許可する）。
FORBIDDEN_WORDS = [
    "勝て", "儲か", "必勝", "おすすめ", "狙い目", "有利", "稼げ", "回収",
]


def fail(msg):
    print(f"NG: {msg}")
    sys.exit(1)


def main():
    with open(GLOSSARY_PATH, encoding="utf-8") as f:
        text = f.read()
    try:
        data = json.loads(text)
    except json.JSONDecodeError as e:
        fail(f"JSONとして読み込めない: {e}")

    if data.get("_meta") != ORIGINAL_META:
        fail("_meta が変更前と一致しない")

    for key, orig in ORIGINAL_ENTRIES.items():
        if key not in data:
            fail(f"既存語が消えている: {key}")
        cur = data[key]
        if cur.get("yomi") != orig["yomi"]:
            fail(f"{key} の yomi が変更されている")
        if cur.get("desc") != orig["desc"]:
            fail(f"{key} の desc が変更されている")
        if cur.get("kind") != orig.get("kind"):
            fail(f"{key} の kind が変更されている（既存語のkindは後付け・変更禁止）")
        orig_alias = orig.get("alias")
        if orig_alias is not None:
            cur_alias = cur.get("alias") or []
            for a in orig_alias:
                if a not in cur_alias:
                    fail(f"{key} の alias から既存の '{a}' が消えている")

    all_keys = [k for k in data.keys() if k != "_meta"]
    if len(all_keys) != len(set(all_keys)):
        fail("キーが重複している")

    alias_owner = {}
    warnings = []
    kind_counts = {"official": 0, "slang": 0, "betting": 0}
    for key, entry in data.items():
        if key == "_meta":
            continue
        if not isinstance(entry, dict):
            fail(f"{key} のエントリが dict でない")
        yomi = entry.get("yomi")
        desc = entry.get("desc")
        if not isinstance(yomi, str) or not yomi:
            fail(f"{key} の yomi が str でない/空")
        if not isinstance(desc, str) or not desc:
            fail(f"{key} の desc が str でない/空")
        alias = entry.get("alias")
        if alias is not None:
            if not isinstance(alias, list) or not all(isinstance(a, str) for a in alias):
                fail(f"{key} の alias が list[str] でない")
            for a in alias:
                if a in all_keys:
                    fail(f"{key} の alias '{a}' が他のキーと衝突している")
                if a in alias_owner and alias_owner[a] != key:
                    fail(f"alias '{a}' が {alias_owner[a]} と {key} で重複している")
                alias_owner[a] = key

        kind = entry.get("kind")
        if key not in ORIGINAL_ENTRIES:
            if kind is not None and kind not in VALID_KINDS:
                fail(f"{key} の kind '{kind}' が official/slang/betting のいずれでもない")
        kind_counts[kind or "official"] += 1

        if key not in ORIGINAL_ENTRIES:
            for word in FORBIDDEN_WORDS:
                if word in desc:
                    fail(f"{key} の desc に禁止語 '{word}' が含まれている")
            for mark in NUMBER_MARKS:
                if mark in desc:
                    warnings.append(f"WARN: {key} の desc に数値記号 '{mark}' が含まれている（構成比の捏造でないか要確認）")

    for w in warnings:
        print(w)

    added = len(all_keys) - len(ORIGINAL_ENTRIES)
    print(
        f"OK: 全チェック通過。総語数={len(all_keys)}（既存{len(ORIGINAL_ENTRIES)} + 追加{added}）"
        f" / kind内訳: official={kind_counts['official']}, slang={kind_counts['slang']}, betting={kind_counts['betting']}"
    )


if __name__ == "__main__":
    main()
